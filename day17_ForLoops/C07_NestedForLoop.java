package day17_ForLoops;

public class C07_NestedForLoop {

	public static void main(String[] args) {
		// 1'den 4'e kadar sayıları yan yana aralarında bir boşluk bırakarak yazdırın.

	
	for (int satır = 1; satır <=5; satır++) {

		for (int i = 1; i <=3; i++) {
		System.out.print(satır*i+" ");
	}
	System.out.println(" ");
	}	
		
		
		
	
	
	
	
	
	
	
	
	
	
	
	}

}
